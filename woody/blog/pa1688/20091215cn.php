<br />2. 在升级前一定要检查PA1688设备的'调试级别'选项. 太多PA1688厂商和客户喜欢把'调试级别'设置成"<a href="../ar1688/20061014cn.php">不检查程序型号</a>", 像这个一样, 就稀里糊涂更新了一个错误的软件. 
<br />3. 在升级后一定要重复检查更新设置的功能是否还能正常使用. 这样可以有效地检查出是否升级了正确的软件. 可惜的是我当时也没有让客户做这一步. 
