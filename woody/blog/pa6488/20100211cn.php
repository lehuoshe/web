<tr><td>2010年2月11日</td></tr>
<tr><td>在<a href="../../../pa1688/indexcn.html">PA1688</a>生产厂商们的强烈要求下, 我们从2003年初开始提供名为"PalmH323"的软件API. 这个名字一直没有改, 尽管后来我们又加入了SIP和IAX2等协议. 当时把软件API和不公开的部分分开还是有不少工作量的, 不过最终我们也通过这个工作整理改善了PA1688软件的总体结构. 当生产厂商们用了几个月后, 我们积极的把API提供给了所有最终用户. 
<br />2005年的时候有个最终用户写电子邮件告诉我们他不能用这个API, 因为我们没有说明任何软件版权, 而他的老板认为这样可能会带来将来法律上的问题. 我想起来了"大教堂和集市"这本书, 就把GPL用做了PA1688软件API的版权协议. 
<br />自然而然的我们在<a href="../../../ar1688/indexcn.html">AR1688</a>刚开始开发的时候就注意保留了软件API和GPL版权协议的特点. 我们在2006年AR1688开发的早期阶段就提供了基于GPL的软件<a href="../ar1688/20061211cn.php">API</a>(打包成sdcc). 2009年的时候一个AR1688的最终用户给我们带来了意外, 他写了好几封很长的电子邮件, 详细说明为什么他的工程师不能在我们基于GPL的软件API基础上开发, 同时建议了好几种解决这个问题的方法. 现在我们采纳了他的建议, 把AR1688软件API的版权协议由GPL改成了BSD. 我希望这个改动合法而且不算太晚. 
<br />我当然也想把PA1688的软件API版权协议改成BSD, 但是现在PA1688跟我们没有关系了. 而且我发现我根本找不到谁还会来跟我讨论PA1688的问题. 
<br />从2009年开始我们开发<a href="../../../pa6488/indexcn.html">PA6488</a>, 同时也计划了基于GPL的软件API. 幸运的是因为我们软件进度太慢, 至今还没有发布任何东西. 目前我们计划在今年6月发布第一个PA6488软件API, 基于BSD版权协议. 
