<tr><td>The key to low cost IP phone is to lower the main processor cost. The key to low cost processor depends on the quantity. 
With VoIP device quantity low for the past 10 years, from the beginning day of the market, nearly all dedicated VoIP chips are doomed to be limited to low quantity and high price. 
This is why <a href="../palmmicro/20061123.php">Sipura</a> can success with VCD/DVD chip, and why more and more VoIP devices are built with router chips. DVD and router are much larger market, and processor for those market are very cheap.
<br /><a href="../../../ar1688/index.html">AR1688</a> is not dedicated VoIP chip neither, it is obvious because it has no built in wired or wireless ethernet. It is also from a large consumer market. 
Actually it is easy to guess by checking the chip features:
<br />1) 8-bit controller up to 48MHz, Z80 instruction compatible
<br />2) 24-bit fixed point DSP up to 60MIPS
<br />3) 116k bytes internal SRAM for controller and DSP
<br />4) Built in 20-bit DAC
<br />5) Built in 18-bit ADC for microphone and line-in
<br />6) Built in DC-DC converter to provide CPU core voltage
<br />7) Support UART
<br />8) Support 8-bit external bus for program flash/LCD/ethernet
