<p>钍是大自然给人类的恩赐, 它会造福全人类, 特别是对中国人. 它会改变人类的生存环境. 它也会改变世界的政治格局. 它也会大大减轻人类对自然资源的争夺, 使得很多难以解决的问题, 比如南海问题, 可以得到解决.
当然这些都是我的预言. 前几天和两位好朋友李祖炜和吴秉渝在公园里散步, 大谈世界百年大计, 把我们的观点用白话说出来. 
